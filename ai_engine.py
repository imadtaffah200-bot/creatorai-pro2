import os
from openai import OpenAI

class مولد_المحتوى_العربي:
    """محرك عربي يستخدم OpenAI فقط لتوليد محتوى احترافي لصناع المحتوى."""

    def __init__(self):
        self.النموذج = os.getenv("OPENAI_MODEL", "gpt-4o-mini")

    def _عميل(self):
        المفتاح = os.getenv("OPENAI_API_KEY")
        if not المفتاح:
            raise RuntimeError("يرجى ضبط المتغير OPENAI_API_KEY قبل استخدام الذكاء الاصطناعي.")
        return OpenAI(api_key=المفتاح)

    def _طلب_نصي(self, تعليمات_النظام, طلب_المستخدم):
        الاستجابة = self._عميل().chat.completions.create(
            model=self.النموذج,
            messages=[
                {"role": "system", "content": تعليمات_النظام},
                {"role": "user", "content": طلب_المستخدم},
            ],
            temperature=0.82,
            max_tokens=1400,
        )
        return الاستجابة.choices[0].message.content.strip()

    def توليد_سكريبت_ريلز(self, الموضوع, اللهجة="العربية الفصحى", المدة="30 ثانية"):
        التعليمات = "أنت كاتب محتوى عربي محترف. اكتب بالعربية، واستعمل اللهجة المطلوبة داخل النص فقط. قدم سكريبت ريلز جاهزاً للتصوير مع افتتاحية جذابة ومشاهد وتعليق صوتي ودعوة للتفاعل."
        الطلب = f"الموضوع: {الموضوع}\nاللهجة: {اللهجة}\nالمدة: {المدة}\nأخرج النتيجة بتنسيق واضح ومباشر."
        return self._طلب_نصي(التعليمات, الطلب)

    def توليد_سيو_يوتيوب(self, الموضوع, الكلمات_المفتاحية="", اللهجة="العربية الفصحى"):
        التعليمات = "أنت خبير سيو يوتيوب للسوق العربي. أنشئ عناوين قوية ووصفاً محسناً وكلمات مفتاحية ووسوماً وفكرة صورة مصغرة."
        الطلب = f"موضوع الفيديو: {الموضوع}\nالكلمات المفتاحية: {الكلمات_المفتاحية}\nاللهجة المستهدفة: {اللهجة}\nأخرج: 7 عناوين، وصفاً كاملاً، وسوماً، كلمات مفتاحية، وخطة نشر مختصرة."
        return self._طلب_نصي(التعليمات, الطلب)

    def توليد_أفكار_صور_مصغرة(self, العنوان, المجال="عام"):
        التعليمات = "أنت مدير إبداعي متخصص في صور يوتيوب المصغرة العربية عالية النقر."
        الطلب = f"اقترح خمس أفكار صور مصغرة احترافية للعنوان: {العنوان}\nالمجال: {المجال}"
        return self._طلب_نصي(التعليمات, الطلب)

    def توليد_خطة_محتوى(self, المجال, اللهجة="العربية الفصحى", عدد_الأيام=7):
        التعليمات = "أنت مخطط محتوى عربي يبني تقاويم نشر واقعية لصناع المحتوى."
        الطلب = f"أنشئ خطة محتوى لمدة {عدد_الأيام} أيام في مجال {المجال} بلهجة {اللهجة}."
        return self._طلب_نصي(التعليمات, الطلب)
